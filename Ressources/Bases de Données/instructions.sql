CREATE TABLE "Case" (
    type TEXT PRIMARY KEY,
    collision INT,
    hauteur INT,
    sortie INT,
    ouvrable INT
);

CREATE TABLE CasesViseesArme (
    arme TEXT REFERENCES Arme(nom),
    x INT,
    y INT,
    PRIMARY KEY (arme, x, y)
);

CREATE TABLE Arme (
    nom TEXT PRIMARY KEY,
    centre_x INT,
    centre_y INT,
    centree_joueur INT,
    puissance INT,
    duree FLOAT,
    attente FLOAT,
    vitesse INT,
    projectiles INT,
    invocation INT
);

CREATE TABLE MondeArme (
    arme TEXT REFERENCES Arme(nom),
    monde TEXT REFERENCES Monde(nom),
    PRIMARY KEY (arme, monde)
);

CREATE TABLE Projectile (
    arme TEXT REFERENCES Arme(nom),
    nom TEXT REFERENCES Arme(nom),
    vitesse INT,
    PRIMARY KEY (arme, nom)
);

CREATE TABLE EffetsArme (
    arme TEXT REFERENCES Arme(nom),
    nom TEXT,
    duree FLOAT,
    PRIMARY KEY (arme, nom)
);

CREATE TABLE Monde (
    nom TEXT PRIMARY KEY,
    sol_par_defaut TEXT REFERENCES "Case"(type),
    mur_par_defaut TEXT REFERENCES "Case"(type),
    sortie_par_defaut TEXT REFERENCES "Case"(type),
    coffre_par_defaut TEXT REFERENCES "Case"(type),
    fontaine_par_defaut TEXT REFERENCES "Case"(type),
    epee_rocher_par_defaut TEXT REFERENCES "Case"(type),
    enclume_par_defaut TEXT REFERENCES "Case"(type),
    mur_supplementaire_par_defaut TEXT REFERENCES "Case"(type)
);

CREATE TABLE VariationCase (
    type TEXT REFERENCES "Case"(type) PRIMARY KEY,
    nombre INT
);

CREATE TABLE ModificationCase (
    type TEXT REFERENCES "Case"(type),
    depart INT,
    fin INT,
    PRIMARY KEY (type, depart)
);

CREATE TABLE ChangementCase (
    depart TEXT REFERENCES "Case"(type),
    fin TEXT REFERENCES "Case"(type),
    PRIMARY KEY (depart, fin)
);

CREATE TABLE MotifSalle (
    salle INT,
    "case" INT,
    x INT,
    y INT,
    PRIMARY KEY (salle, x, y)
);

CREATE TABLE Monstre (
    nom TEXT PRIMARY KEY
);

CREATE TABLE MondeMonstre (
    monstre TEXT REFERENCES Monstre(nom),
    monde TEXT REFERENCES Monde(nom)
);

CREATE TABLE StatistiqueMonstre (
    monstre TEXT REFERENCES Monstre(nom),
    categorie TEXT,
    sous_categorie TEXT,
    valeur INT,
    PRIMARY KEY (monstre, categorie, sous_categorie)
);

CREATE TABLE ArmeMonstre (
    monstre TEXT REFERENCES Monstre(nom),
    arme TEXT REFERENCES Arme(nom),
    coefficient INT,
    PRIMARY KEY (monstre, arme)
);

CREATE TABLE Personnage (
    nom TEXT PRIMARY KEY
);

CREATE TABLE MondePersonnage (
    personnage TEXT REFERENCES Personnage(nom),
    monde TEXT REFERENCES Monde(nom)
);

CREATE TABLE StatistiquePersonnage (
    personnage TEXT REFERENCES Personnage(nom),
    categorie TEXT,
    sous_categorie TEXT,
    valeur INT,
    PRIMARY KEY (personnage, categorie, sous_categorie)
);

CREATE TABLE ArmePersonnage (
    personnage TEXT REFERENCES Personnage(nom),
    arme TEXT REFERENCES Arme(nom),
    PRIMARY KEY (personnage, arme)
);

CREATE TABLE Interaction (
    depart TEXT REFERENCES Monde(nom),
    arrivee TEXT REFERENCES Monde(nom),
    valeur INT,
    PRIMARY KEY (depart, arrivee)
);

CREATE TABLE PNJ (
    monde TEXT REFERENCES Monde(nom),
    nom TEXT,
    PRIMARY KEY (monde, nom)
);

CREATE TABLE Enchantement (
    id INT PRIMARY KEY,
    nom_trad TEXT,
    lien_fichier TEXT,
    prix INT
);

CREATE TABLE PossessionEnchantementMonstre (
    enchantement_id INT REFERENCES Enchantement(id),
    posseder_par_id INT REFERENCES Monstre
);

